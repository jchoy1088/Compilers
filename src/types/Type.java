package types;

public class Type {

	public String tInteger = "int";
	public String tString = "string";
	public String tChar = "char";
	public String tDouble = "double";
	public String tFloat = "float";
	public String tBoolean = "boolean";

	public String TypeInteger() {
		return tInteger;
	}

	public String TypeString() {
		return tString;
	}

	public String TypeChar() {
		return tChar;
	}

	public String TypeDouble() {
		return tDouble;
	}

	public String TypeFloat() {
		return tFloat;
	}

	public String TypeBoolean() {
		return tBoolean;
	}

}
